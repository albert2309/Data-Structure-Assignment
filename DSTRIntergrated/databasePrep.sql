DROP TABLE customerList;
DROP TABLE transactionList;
	CREATE TABLE customerList (
	account_number VARCHAR[20] PRIMARY KEY,
	card_ver_code INT NOT NULL,
	expiry_date VARCHAR[5] NOT NULL,
	first_name VARCHAR[255] NOT NULL,
	last_name VARCHAR[255] NOT NULL,
	date_of_birth VARCHAR[9] NOT NULL,
	address VARCHAR[255] NOT NULL,
	balance FLOAT NOT NULL
);

CREATE TABLE transactionList (
	account_number VARCHAR[16],
	type_of_transaction VARCHAR[10] NOT NULL,
	amount FLOAT NOT NULL,
	target_account VARCHAR[16],
	date_of_transaction VARCHAR[9] NOT NULL,
	FOREIGN KEY (account_number) REFERENCES customerList(account_number)
);
